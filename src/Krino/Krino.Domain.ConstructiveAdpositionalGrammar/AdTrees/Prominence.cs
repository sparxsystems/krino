namespace Krino.Domain.ConstructiveAdpositionalGrammar.AdTrees
{
    /// <summary>
    /// Information prominence settings.
    /// </summary>
    public enum Prominence
    {
        /// <summary>
        /// The information prominence is not defined.
        /// </summary>
        Undefined,

        /// <summary>
        /// The information prominence stays by this element.
        /// </summary>
        Prominent,

        /// <summary>
        /// The information prominence does not stay by this element.
        /// </summary>
        NotProminent,
    }
}
